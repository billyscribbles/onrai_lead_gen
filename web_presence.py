"""Pure web-presence classification for the no-website lead finder.

Two tiers, both free of Apify calls:

  Tier A — ``classify_website`` reads the Google Maps ``website`` field and
  decides whether a business has no site, only a social/link-aggregator page, an
  http-only site, or a real https site. No network needed.

  Tier B — ``classify_live_site`` judges a real https site from the result of a
  local HTTP fetch (broken / parked / not-mobile / healthy). The fetch itself is
  I/O and lives in the orchestration script; the *decision* is pure and tested
  here.

A business is a lead unless its site is ``healthy`` (see ``is_lead_status``).
"""

import math
from urllib.parse import quote_plus

_AU_STATES = {"VIC", "NSW", "QLD", "SA", "WA", "TAS", "NT", "ACT"}


def _suburb_from_address(address):
    """Best-effort suburb from an Australian-format address string."""
    for part in address.split(","):
        tokens = part.strip().split()
        if any(t.upper() in _AU_STATES for t in tokens):
            kept = [t for t in tokens if t.upper() not in _AU_STATES and not t.isdigit()]
            if kept:
                return " ".join(kept)
    return ""


def extract_suburb(place):
    """Suburb from neighborhood/city, falling back to parsing the address."""
    for key in ("neighborhood", "city"):
        val = place.get(key)
        if val:
            return val.strip()
    address = place.get("address")
    if address:
        return _suburb_from_address(address)
    return ""


def dedupe_by_place_id(places):
    """Drop duplicate places sharing a placeId; places without an id are kept."""
    seen = set()
    result = []
    for place in places:
        pid = place.get("placeId")
        if pid:
            if pid in seen:
                continue
            seen.add(pid)
        result.append(place)
    return result


def parse_suburb_lines(text):
    """Parse a one-per-line file (suburbs or categories), ignoring blanks and # comments."""
    items = []
    for line in text.splitlines():
        line = line.strip()
        if line and not line.startswith("#"):
            items.append(line)
    return items


